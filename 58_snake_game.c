void snake_head_coordinates(int *x, int *y)
{
    for (int i = 0; i < 19; i++)
    {
        for (int j = 0; j < 32; j++)
        {
            if (screen[i][j] == 'O')
            {
                *x = i;
                *y = j;
            }
        }
    }
}

void move_to_right()
{
    for (int i = 0; i < 19; i++)
    {
        for (int j = 0; j < 32; j++)
        {
            if (screen[i][j] == 'O')
            {
                screen[i][j] = ' ';
                screen[i][j + 1] = 'O';
                j++;
            }
        }
    }
}
void move_to_left()
{
    for (int i = 0; i < 19; i++)
    {
        for (int j = 0; j < 32; j++)
        {
            if (screen[i][j] == 'O')
            {
                screen[i][j] = ' ';
                screen[i][j - 1] = 'O';
            }
        }
    }
}
void move_up()
{
    for (int i = 0; i < 19; i++)
    {
        for (int j = 0; j < 32; j++)
        {
            if (screen[i][j] == 'O')
            {
                screen[i][j] = ' ';
                screen[i - 1][j] = 'O';
            }
        }
    }
}
void move_down()
{
    for (int i = 0; i < 19; i++)
    {
        for (int j = 0; j < 32; j++)
        {
            if (screen[i][j] == 'O')
            {
                screen[i][j] = ' ';
                screen[i + 1][j] = 'O';
                i++;
            }
        }
    }
}

