void listen_to_key_press()
{
    // for unix

    while (1)
    {
        system("stty raw");
        char c = getchar();

        switch (c)
        {
        case 'j':
            system("stty cooked");
            move_to_left();
            break;
        case 'l':
            system("stty cooked");
            move_to_right();
            break;
        case 'i':
            system("stty cooked");
            move_up();
            break;
        case 'k':
            system("stty cooked");
            move_down();
            break;
        default:
            system("stty cooked \n");
            exit(0);
        }
        rerender(c);
    }
}

